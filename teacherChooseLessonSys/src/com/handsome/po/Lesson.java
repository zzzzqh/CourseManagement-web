package com.handsome.po;

public class Lesson {
	
	private int gread;
	private String subject;
	private int professionalNumber;
	private String className;
	private String type;
	private String credit;
	private int hours;
	private int experimentClass;
	private int onComputerClassHour;
	private String theOrderOfWeeks;
	private String remark;
	
	public int getGread() {
		return gread;
	}
	public void setGread(int gread) {
		this.gread = gread;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public int getProfessionalNumber() {
		return professionalNumber;
	}
	public void setProfessionalNumber(int professionalNumber) {
		this.professionalNumber = professionalNumber;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCredit() {
		return credit;
	}
	public void setCredit(String credit) {
		this.credit = credit;
	}
	public int getHours() {
		return hours;
	}
	public void setHours(int hours) {
		this.hours = hours;
	}
	public int getExperimentClass() {
		return experimentClass;
	}
	public void setExperimentClass(int experimentClass) {
		this.experimentClass = experimentClass;
	}
	public int getOnComputerClassHour() {
		return onComputerClassHour;
	}
	public void setOnComputerClassHour(int onComputerClassHour) {
		this.onComputerClassHour = onComputerClassHour;
	}
	public String getTheOrderOfWeeks() {
		return theOrderOfWeeks;
	}
	public void setTheOrderOfWeeks(String theOrderOfWeeks) {
		this.theOrderOfWeeks = theOrderOfWeeks;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
}
